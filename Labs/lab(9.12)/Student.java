public class Student{
    private int studNumber;
    private boolean studActive;
    private double stuGPA;
    private String stuName;
    public Student(){
        // initialise instance variables
        studNumber = 0;
        studActive = false;
        stuGPA = 0;
        stuName = "";
    }
    public void setStudNumber(int sn)    {
        studNumber = sn;
    }
    public int getStudNumber()    {
        return studNumber;
    }
    public void setStudActive(int sn)    {
        studNumber = sn;
    }
    public int getStudActive()    {
        return studNumber;
    }
    public void setStuGPA(int sn)    {
        studNumber = sn;
    }
    public int getStuGPA()    {
        return studNumber;
    }
    public void setStuName(int sn)    {
        studNumber = sn;
    }
    public int getStuName()    {
        return studNumber;
    }
}